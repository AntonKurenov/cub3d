#include "printf.h"

int	atoi_str(char **str)
{
	unsigned int ret;
	int m;

	ret = 0;
	m = 1;
	while (**str == ' ')
		(*str)++;
	if (**str == '-')
		m = -1;
	if (**str == '-' || **str == '+')
		(*str)++;
	while (**str >= '0' && **str <= '9' && **str)
	{
		ret = ret * 10 + (**str - '0');
		(*str)++;
	}
	return (ret * m);
}
